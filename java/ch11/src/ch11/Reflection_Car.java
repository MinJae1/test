package ch11;

public class Reflection_Car {
	private String model;
	private String owner;

	public String getModel() {
		return model;
	}

	public String getOwner() {
		return owner;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}
}
